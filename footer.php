           $(document).on('change', '.qty', function() {
               var row = $(this).closest('tr');
               var qty = $(this).val();
               var c_id = row.find('td:eq(0)').text();
               var product_name = row.find('td:eq(2) a').text();
               var product_price = row.find('td:eq(3)').text();

               $.ajax({
                   url: 'ajaxupquan.php',
                   type: 'POST',
                   data: {
                       CartUpdate: 1,
                       c_id: c_id,
                       c_qty: qty,
                       product_name: product_name,
                       product_price: product_price
                   },
                   success: function(response) {
                       if (response == 'success') {
                           // Update subtotal value
                           var subtotal = parseFloat(product_price) * parseInt(qty);
                           row.find('.subtotal').text(subtotal);
                       } else {
                           alert("Failed to update quantity!");
                       }
                   },
                   error: function(err) {
                       alert("API call failed: " + err);
                   }
               });
           });



           $("#search").keyup(function() {
               var input = $(this).val();
               // alert(input);
               if (input != "") {
                   $.ajax({
                       url: "livesearch.php",
                       method: "POST",
                       data: {
                           search_term: input
                       },
                       success: function(data) {
                           $('.section-bg-1,.mt-120,.ltn__product-tab-area,.ltn__call-to-action-area,.ltn__product-area,.ltn__img-slider-area,.ltn__blog-area').hide();
                           $("#search-result").html(data);
                           $("#search-result").css("display", "block");
                       }
                   })
               } else {
                   $('.section-bg-1,.mt-120,.ltn__product-tab-area,.ltn__call-to-action-area,.ltn__product-area,.ltn__img-slider-area,.ltn__blog-area').show();
                   $("#search-result").css("display", "none");
               }
           })

